import React, { useState, useEffect } from 'react';
import { Link, useLocation } from 'react-router-dom';
import { Menu, X, Code2 } from 'lucide-react';
import { motion, AnimatePresence } from 'framer-motion';

const Navbar: React.FC = () => {
  const [isOpen, setIsOpen] = useState(false);
  const [scrolled, setScrolled] = useState(false);
  const location = useLocation();

  useEffect(() => {
    const handleScroll = () => {
      setScrolled(window.scrollY > 20);
    };
    window.addEventListener('scroll', handleScroll);
    return () => window.removeEventListener('scroll', handleScroll);
  }, []);

  const navLinks = [
    { name: 'Home', path: '/' },
    { name: 'About', path: '/about' },
    { name: 'Events', path: '/events' },
    { name: 'Council', path: '/council' },
    { name: 'Contact', path: '/contact' },
  ];

  return (
    <nav
      className={`fixed top-0 w-full z-50 transition-all duration-300 ${
        scrolled ? 'bg-primary/80 backdrop-blur-md border-b border-white/5 py-4' : 'bg-transparent py-6'
      }`}
    >
      <div className="max-w-7xl mx-auto px-4 sm:px-6 flex justify-between items-center">
        {/* Logo */}
        <Link to="/" className="flex items-center gap-2 sm:gap-3 group">
          <img 
            src="/images/codecrafters_logo.jpg" 
            alt="CodeCrafters Logo" 
            className="w-10 h-10 sm:w-12 sm:h-12 rounded-full object-cover transform group-hover:scale-110 transition-transform shadow-lg border-2 border-orange-500/30"
          />
          <div className="flex flex-col">
            <span className="font-heading font-bold text-lg sm:text-xl tracking-tight text-white group-hover:text-orange-400 transition-colors">
              CodeCrafters
            </span>
            <span className="text-[9px] sm:text-[10px] text-gray-400 uppercase tracking-widest hidden xs:block">IITM BS Degree</span>
          </div>
        </Link>

        {/* Desktop Nav */}
        <div className="hidden lg:flex items-center gap-6 xl:gap-8">
          {navLinks.map((link) => (
            <Link
              key={link.name}
              to={link.path}
              className={`relative font-medium text-sm tracking-wide transition-colors hover:text-orange-400 ${
                location.pathname === link.path ? 'text-orange-500' : 'text-gray-300'
              }`}
            >
              {link.name}
              {location.pathname === link.path && (
                <motion.div
                  layoutId="underline"
                  className="absolute -bottom-1 left-0 right-0 h-0.5 bg-orange-500 shadow-[0_0_10px_rgba(249,115,22,0.8)]"
                />
              )}
            </Link>
          ))}
          <a
            href="https://docs.google.com/forms/d/e/1FAIpQLSc0Ek_SsHqyMG7YBmPhzqpW-xde76Uga8KdWl8-GE4xGdrClw/viewform"
            target="_blank"
            rel="noopener noreferrer"
            className="px-5 xl:px-6 py-2.5 rounded-full bg-gradient-to-r from-orange-600 to-red-600 text-white font-bold text-sm hover:shadow-[0_0_25px_rgba(249,115,22,0.6)] transition-all transform hover:-translate-y-1 hover:scale-105 border border-orange-500/30"
          >
            Join Us
          </a>
        </div>

        {/* Mobile Toggle */}
        <button
          className="lg:hidden text-white z-50"
          onClick={() => setIsOpen(!isOpen)}
        >
          {isOpen ? <X size={24} /> : <Menu size={24} />}
        </button>
      </div>

      {/* Mobile Menu */}
      <AnimatePresence>
        {isOpen && (
          <motion.div
            initial={{ opacity: 0, height: 0 }}
            animate={{ opacity: 1, height: 'auto' }}
            exit={{ opacity: 0, height: 0 }}
            className="lg:hidden bg-slate-900/95 backdrop-blur-xl border-b border-white/10 overflow-hidden"
          >
            <div className="flex flex-col p-6 gap-4">
              {navLinks.map((link) => (
                <Link
                  key={link.name}
                  to={link.path}
                  onClick={() => setIsOpen(false)}
                  className={`text-base sm:text-lg font-medium py-2 ${
                    location.pathname === link.path ? 'text-orange-500' : 'text-gray-300'
                  } hover:text-orange-400 transition-colors`}
                >
                  {link.name}
                </Link>
              ))}
              <a
                href="https://docs.google.com/forms/d/e/1FAIpQLSc0Ek_SsHqyMG7YBmPhzqpW-xde76Uga8KdWl8-GE4xGdrClw/viewform"
                target="_blank"
                rel="noopener noreferrer"
                onClick={() => setIsOpen(false)}
                className="mt-2 text-center py-3 rounded-lg bg-gradient-to-r from-orange-600 to-red-600 text-white font-bold hover:shadow-lg transition-all border border-orange-500/30"
              >
                Join Us
              </a>
            </div>
          </motion.div>
        )}
      </AnimatePresence>
    </nav>
  );
};

export default Navbar;
